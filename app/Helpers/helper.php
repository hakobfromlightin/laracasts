/**
 * Convert date to human friendly.
 *
 * @param      $date
 * @param null $short
 * @param null $weekDay
 *
 * @return bool|string
 */
function date_to_human_friendly($date, $short = null, $weekDay = null)
{
    if (is_object($date)) {
        return $date->format('l, d F Y');
    }

    if (is_string($date)) {
        $date = strtotime($date);
    }

    if ($short) {
        return date('l, d F', $date);
    }
    if ($weekDay) {
        return date('d F Y', $date);
    }

    return date('l, d F Y', $date);
}


/**
 * Get number of days between two dates.
 *
 * @param $start_date
 * @param $end_date
 * @param $type
 *
 * @return float
 */
function get_days_between_dates($start_date, $end_date, $type)
{
    $start_date = strtotime($start_date);
    $end_date = strtotime($end_date);
    $date_diff = $end_date - $start_date;
    $days = intval(floor($date_diff / (60 * 60 * 24)));

    if ($type == 'booking' || $days == 0) {
        return $days + 1;
    }

    return $days;
}

